//function that doubles a number
function double(num) {
    return num * 2;
}

//function that returns the square of a number
function square(num) {
    return num * num;
}

//function that tests if a number is even
function isEven(num) {
    return num % 2 === 0;
}

//function that tests if a number is odd
function isOdd(num) {
    return num % 2 !== 0;
}

//function that multiplies two numbers together.
function multiply(num1, num2) {
    return num1 * num2;
}